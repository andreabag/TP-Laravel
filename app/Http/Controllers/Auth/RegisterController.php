    /**
     * Create a new user instance after a valid registration.
     *
     * @param  array  $data
     * @return \App\User
     */
    protected function create(array $data)
    {
        return User::create([
            'name' => $data['name'],
            'surname' => $data['surname'],
            'user' => $data['user'],
            'email' => $data['email'],
            'birthdate' => $data['birthdate'],
            'province' => $data['province'],
            'avatar' => $data['avatar'],
            'password' => Hash::make($data['password']),
        ]);
    }
